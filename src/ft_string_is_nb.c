#include "header.h"

int		ft_string_is_nb(char *str)
{
	int	i;
	i = 0;

	if (str[0] == '-' && str[1])
		i++;
	while (str[i] != 0)
	{
		if (!ft_isdigit(str[i]))
			return (-1);
		i++;
	}
	return (0);
}
